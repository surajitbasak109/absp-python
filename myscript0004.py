### Write a short program that prints the numbers 1 to 10 using a for loop.
### Then write an equivalent program that prints the numbers 1 to 10
###  using a while loop. ###

# using for loop
print('Using "for" loop')
for i in range(1,11):
	print(i)

# using while loop
print('Using "while" loop')
i = 1
while i < 11:
	print(i)
	i = i +1

